class Product {
  final String name;
  final String id;
  final String productImage;
  final double price;
  final double sellingPrice;
  final String description;
  final String category;

  Product({
    required this.name,
    required this.id,
    required this.productImage,
    required this.price,
    required this.sellingPrice,
    required this.description,
    required this.category,
  });
}